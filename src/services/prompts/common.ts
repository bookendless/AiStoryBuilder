/**
 * プロンプト共通定数・ヘルパー
 *
 * 全プロンプトファイルで共有する出力ルールとデータ境界マーカーを定義する。
 *
 * ## データ境界マーカーについて
 * aiService.generateContent は送信前にプロンプト全体へ sanitizeInputForPrompt を適用し、
 * `<` `>` を除去する（securityUtils.ts のXSS対策）。そのため長文データの境界には
 * XMLタグではなく、除去されない【ラベル（ここから）】/【ラベル（ここまで）】形式の
 * マーカーを使用する。ユーザー由来・AI生成由来の長文（本文・あらすじ・要約・会話履歴など）を
 * このマーカーで囲むことで、モデルがデータ内の文を指示と誤認することを防ぐ。
 */

/**
 * 長文データをラベル付き境界マーカーで囲む。
 * 例: dataBlock('本文', '{currentText}') →
 *   【本文（ここから）】
 *   {currentText}
 *   【本文（ここまで）】
 */
export const dataBlock = (label: string, content: string): string =>
    `【${label}（ここから）】\n${content}\n【${label}（ここまで）】`;

/**
 * JSON出力の統一ルール。
 * parseAIResponse / parseJsonLoose はコードブロック付きJSONも解析できるが、
 * 逸脱の余地を減らすため「JSONのみ・コードブロックなし」に統一する。
 */
export const JSON_OUTPUT_RULES = `【出力ルール（厳守）】
- 指定されたJSON形式のみを出力する（前置き・説明文・コメントは書かない）
- コードブロック（三重のバッククォート）で囲まない
- キー名と構造は指定どおりに維持する`;

/**
 * 散文生成の統一ルール（本文のみを出力させる）。
 * @param outputName 出力物の呼称（例:「本文」「あらすじ」「日記」）
 */
export const textOnlyOutputRule = (outputName: string): string =>
    `【出力ルール（厳守）】
- ${outputName}のみを出力する（前置き・説明文・見出し・コメントは書かない）`;

