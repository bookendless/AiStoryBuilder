/**
 * 章分割ツールのAI提案用プロンプトビルダー
 *
 * AIには章境界の「ロケータ」（新しい章が始まる行の逐語コピー）だけを返させ、
 * 本文のスライス自体はコード（findLocatorOffset + sliceByBoundaries）が行う。
 * これにより、AIを使う経路でも原文の逐語保存（join('') === 原文）が崩れない。
 */

/** 本文（または分割チャンク）から章境界ロケータを提案させる */
export function buildBoundaryLocatorPrompt(body: string, part: number, total: number): string {
    const scope = total > 1 ? `（全${total}部分中の第${part}部分）` : '';
    return `次の小説本文${scope}を読み、新しい章が始まると判断できる位置を挙げてください。

【本文（ここから）】
${body}
【本文（ここまで）】

【最重要ルール】
- locator には「新しい章の最初の行」を本文から一字一句そのままコピーする（10〜40文字。行が短ければ行全体）
- 本文に存在しない文字列を locator に出力してはならない（要約・言い換え・創作の禁止）
- 場面転換の記号（◇◆◇ や *** など）だけでは章境界としない
- 章境界が見つからなければ "boundaries": [] とする
- title は見出し行があればそれを、無ければ章内容に即した簡潔な題（20文字以内）

【出力形式】
次のJSON形式のみを出力してください。説明やコメントは不要。
{
  "boundaries": [
    { "title": "章タイトル案", "locator": "新しい章の最初の行を一字一句コピー" }
  ]
}`;
}
